	.arch	i186
	.code16
	.intel_syntax noprefix

#include "common.inc"

/**
 * INT 17h AH=05h - sys_set_sleep_time
 * Input:
 * - BL = Time until sleep when idle (minutes)
 * Output:
 */
	.global sys_set_sleep_time
sys_set_sleep_time:
	push ax
	push bx

	mov al, bl
	mov bx, BIOS_SRAM_CONFIG1
	call __sys_write_sram_byte

	pop bx
	pop ax
	ret
