	.arch	i186
	.code16
	.intel_syntax noprefix

#include "../common.inc"

/**
 * INT 12h AH=0Dh - sprite_get_char
 * Input:
 * - BX = sprite ID
 * Output:
 * - AX = sprite attribute
 */
    .global sprite_get_char
sprite_get_char:
    push di

    call __display_sprite_at
    ss mov ax, [di]
    
    pop di
    ret
